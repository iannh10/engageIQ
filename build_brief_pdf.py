        bullet("Replace the batch ingestion script with scheduled GH Archive polling and webhook listeners.", s["bullet"]),
        bullet("Add a held-out relevance set and replace Precision@10 with NDCG@10 to remove the ceiling effect.", s["bullet"]),
        bullet("<b>Implemented:</b> per-card suggested actions are now generated by Claude Haiku using the opportunity title, platform, domain, and persona profile, cached per (opportunity, persona) pair.", s["bullet"]),
