"""Build brief.pdf (≈4 pages) from the technical-brief content.

Run: python3 build_brief_pdf.py
Output: brief.pdf at project root.
"""

from __future__ import annotations

from pathlib import Path

from reportlab.lib import colors
from reportlab.lib.pagesizes import letter
from reportlab.lib.styles import ParagraphStyle, getSampleStyleSheet
from reportlab.lib.units import inch
from reportlab.platypus import (
    PageBreak,
    Paragraph,
    Preformatted,
    SimpleDocTemplate,
    Spacer,
    Table,
    TableStyle,
)


ROOT = Path(__file__).resolve().parent
OUT = ROOT / "brief.pdf"


def build_styles():
    base = getSampleStyleSheet()
    styles = {
        "title": ParagraphStyle(
            "title", parent=base["Title"], fontSize=18, leading=22,
            spaceAfter=3, textColor=colors.HexColor("#0B5A43"),
        ),
        "subtitle": ParagraphStyle(
            "subtitle", parent=base["Normal"], fontSize=9, leading=12,
            spaceAfter=1, textColor=colors.HexColor("#444"),
        ),
        "h1": ParagraphStyle(
            "h1", parent=base["Heading1"], fontSize=13, leading=16,
            spaceBefore=8, spaceAfter=4, textColor=colors.HexColor("#0B5A43"),
        ),
        "h2": ParagraphStyle(
            "h2", parent=base["Heading2"], fontSize=10.5, leading=14,
            spaceBefore=6, spaceAfter=3, textColor=colors.HexColor("#16A691"),
        ),
        "body": ParagraphStyle(
            "body", parent=base["BodyText"], fontSize=9, leading=12,
            spaceAfter=4, alignment=0,
        ),
        "bullet": ParagraphStyle(
            "bullet", parent=base["BodyText"], fontSize=9, leading=12,
            leftIndent=14, bulletIndent=2, spaceAfter=2,
        ),
        "mono": ParagraphStyle(
            "mono", parent=base["Code"], fontSize=7, leading=8.6,
            textColor=colors.HexColor("#0f1c17"),
        ),
        "caption": ParagraphStyle(
            "caption", parent=base["Italic"], fontSize=8, leading=10,
            textColor=colors.HexColor("#666"), spaceAfter=6,
        ),
    }
    return styles


def p(text, style):
    return Paragraph(text, style)


def bullet(text, style):
    return Paragraph(f"• {text}", style)


